                            <h2>Bunny NameGen</h2>
                            <hr class="star-primary">
                            <img src="img/portfolio/nameGen-Screenshot001.png" class="img-responsive img-centered" alt="">
                            <h3>About</h3>

							<p>I design Magic: The Gathering cards occasionally and I found that I was mostly terrible at coming up with names for any legendary things or for organisation/kingdom/clan names. I used to use Chris Pound's site for bits and pieces but I didn't like waiting for the site to update it's list so I ported the scripts over to C# and dropped them into a simple WinForm.</p>

							<p>By default, it will open any xml in the XMLDatasets folder and any txt files found in the Datasets folder. XML datasets have priority and so a txt dataset with the same name as an XML dataset will NOT be loaded at all. It can also load a dataset through the file menu and that dataset will be tacked onto the end of the list.</p>

							<p>Most of the datasets are from Chris Pound's site with a few added by myself. The scripts I based my work on can be found here: <a href="http://generators.christopherpound.com/">http://generators.christopherpound.com/</a></p>
							
							<p>I am currently working on porting it to being a webapp.</p>

							<h3>Dataset Format</h3>

							<p>Each dataset is either a simple xml file or a txt file containing all the words separated by spaces and new lines. The program currently accepts no other form of dataset.</p>

